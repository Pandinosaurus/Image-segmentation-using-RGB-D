def evaluate_segmentation_per_image(pred, label, num_classes):
    flat_pred = pred.flatten()
    flat_label = label.flatten()

    #iou,p_c_iu,cl = mean_IU(pred, label, num_classes)
    #global_accuracy = mean_accuracy(pred, label)
    pixel_accuracy = compute_global_accuracy(flat_pred, flat_label)
